package com.driver_hiring.user.models;

public class UserModel {
    private String uid,name,dob,gender, email, contact,  address, city, state, pincode;

    public UserModel(String uid, String name, String dob, String gender, String email, String contact
            , String address, String city, String state, String pincode) {
        this.uid = uid;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.email = email;
        this.contact = contact;
        this.address = address;
        this.city = city;
        this.state = state;
        this.pincode = pincode;
    }


    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }
}
